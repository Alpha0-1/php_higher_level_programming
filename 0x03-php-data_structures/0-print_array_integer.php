<?php
/**
 * 0-print_array_integer.php
 *
 * This script prints all integers in an array, each on a new line.
 */

// Example array
$numbers = [10, 20, 30, 40, 50];

// Loop through array and print each element
foreach ($numbers as $number) {
    echo $number . PHP_EOL;
}

/**
 * Output:
 * 10
 * 20
 * 30
 * 40
 * 50
 */
?>
